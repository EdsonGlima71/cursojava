    private void btnCliSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCliSalvarActionPerformed
        
        Clientes c = new Clientes();
        ClientesDAO dao = new ClientesDAO();
        
        c.setNome(txtCliNome.getText());
        c.setEndereco(txtCliLogradouro.getText());
        c.setBairro(txtCliBairro.getText());
        c.setCidade(txtCliCidade.getText());
        c.setEstado((String) cbCliEstado.getSelectedItem());
        c.setCep(txtCliCep.getText());
        c.setCpf(txtCliCpf.getText());
        c.setCelular(txtCliCelular.getText());
        c.setTipo((String) cbCliTipo.getSelectedItem());
        dao.create(c);
        
        JOptionPane.showMessageDialog(null, "Cliente Cadastrado com Sucesso!.");
        
        // LIMPA OS CAMPOS DO FORMULARIO
        txtCliNome.setText("");
        txtCliLogradouro.setText("");
        txtCliBairro.setText("");
        txtCliCidade.setText("");
        cbCliEstado.setSelectedIndex(-1);
        txtCliCep.setText("");
        txtCliCpf.setText("");
        txtCliCelular.setText("");
        cbCliTipo.setSelectedIndex(-1);
        
    }//GEN-LAST:event_btnCliSalvarActionPerformed

