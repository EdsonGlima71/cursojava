    private void btnCliListAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCliListAtualizarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel)tblClientes.getModel();
        modelo.setNumRows(0);
         
        ClientesDAO dao = new ClientesDAO();
        
        try {
           for(Clientes c : dao.read()){
               
               modelo.addRow(new Object[]{
                   c.getNome(),
                   c.getEndereco(),
                   c.getBairro(),
                   c.getCidade(),
                   c.getEstado(),
                   c.getCep(),
                   c.getCpf(),
                   c.getCelular(),
                   c.getTipo()
                   
               });
           
           } 
            
        } catch (Exception e) {
        }
               
    }//GEN-LAST:event_btnCliListAtualizarActionPerformed

